// â”€â”€â”€ All available field types â”€â”€â”€
export type FieldType =
  // Inputs
  | "text"
  | "email"
  | "number"
  | "currency"
  | "password"
  | "url"
  | "tel"
  | "date"
  | "datetime-local"
  | "file"
  | "checkbox"
  | "radio-group"
  | "textarea"
  | "select"
  // Structural / visual blocks
  | "header"
  | "spacer";

// â”€â”€â”€ Option shape for select, radio, checkbox â”€â”€â”€
export interface FieldOption {
  label: string;
  value: string;
  checked?: boolean; // used for checkbox
  default?: boolean; // used for radio-group & select
}

// â”€â”€â”€ Supported masking types â”€â”€â”€
export type MaskType =
  | "creditCard"
  | "ssn"
  | "zip"
  | "usPostal"
  | "tel"
  | "email"
  | "url"
  | "currency"
  | "decimal"
  | "time"
  | "alphanumeric"
  | "slug"
  | "custom";

// â”€â”€â”€ Field configuration â”€â”€â”€
export interface FieldConfig {
  // Identity
  id: number;
  type: FieldType;

  // Label = user-facing text
  // For "header": displayed heading
  // For inputs: field label
  label: string;

  // Machine identifier
  // For structural blocks (header/spacer), can be autogenerated
  name: string;

  // â”€â”€â”€ Layout & styling â”€â”€â”€
  layout?: "full" | "half";
  align?: "start" | "center" | "end";
  justify?: "start" | "center" | "end" | "between";
  margin?: string;  // Tailwind margin utility (e.g. "mb-4")
  padding?: string; // Tailwind padding utility (e.g. "p-2")

  // â”€â”€â”€ Common input props â”€â”€â”€
  required?: boolean;
  placeholder?: string;

  // â”€â”€â”€ Text validation â”€â”€â”€
  minlength?: number;
  maxlength?: number;
  exactLength?: number;
  pattern?: string;
  maskType?: MaskType;

  // â”€â”€â”€ Character restrictions â”€â”€â”€
  alphaOnly?: boolean;
  noWhitespace?: boolean;
  uppercaseOnly?: boolean;
  lowercaseOnly?: boolean;

  // â”€â”€â”€ Extra validation rules â”€â”€â”€
  startsWith?: string;
  endsWith?: string;
  contains?: string;
  minWords?: number;
  maxWords?: number;
  allowedValues?: string[];
  disallowedValues?: string[];
  matchField?: string;
  customErrorMessage?: string;

  // â”€â”€â”€ Number validation â”€â”€â”€
  minValue?: number;
  maxValue?: number;
  step?: number | "any";
  noNegative?: boolean;
  positiveOnly?: boolean;
  integerOnly?: boolean;
  decimalPlaces?: number;

  // â”€â”€â”€ Date validation â”€â”€â”€
  minDate?: string; // "YYYY-MM-DD" or ISO datetime
  maxDate?: string;

  // â”€â”€â”€ Options (checkbox / radio / select) â”€â”€â”€
  options?: FieldOption[];
  orientation?: "vertical" | "horizontal";
  isMulti?: boolean;

  // â”€â”€â”€ File uploads â”€â”€â”€
  accept?: string;
  multiple?: boolean;
  maxFileSizeMB?: number;

  // â”€â”€â”€ Textarea â”€â”€â”€
  rows?: number;
  cols?: number;

  // â”€â”€â”€ Header specific â”€â”€â”€
  level?: "h1" | "h2" | "h3" | "h4" | "h5";

  // â”€â”€â”€ Spacer specific â”€â”€â”€
  spacerSize?: "sm" | "md" | "lg" | "xl" | number;
  hideOnMobile?: boolean;   // ðŸ”¥ NEW: allows toggle to hide spacer in mobile preview
}
